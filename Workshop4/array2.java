class array2{
    public static void main(String[] args){
      //create an array
      int[] type= {12, 4, 5};
      //loop through the array
      //using for loop
      System.out.println("Using for loop:");
      for(int i = 0; i < type.length; i++){
          System.out.println(type[i]);
      }
    }
}